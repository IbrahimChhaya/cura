package MapLocation;

public class Ratings {
    private String name;
    private float rating;


    // Getter Methods

    public String getName() {
        return name;
    }

    public float getRating() {
        return rating;
    }

    // Setter Methods

    public void setName(String name) {
        this.name = name;
    }

    public void setRating(float rating) {
        this.rating = rating;
    }
}
